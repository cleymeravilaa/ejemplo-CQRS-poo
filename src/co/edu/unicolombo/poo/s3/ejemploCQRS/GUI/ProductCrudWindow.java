    private boolean verifycationField(JTextField field, String message) {
        String data = field.getText();
        data = data.trim();
        if(data.isEmpty()) {
            field.requestFocus();
            field.setBackground(Color.red);
            JOptionPane.showMessageDialog(this, message, "Validar", JOptionPane.ERROR_MESSAGE);
            return false;
        } else {
            field.setBackground(Color.WHITE);
            return true;
        }   
    }
    
