    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            
            if (!verifycationField(fieldName, "Name cannot be null")) {
                return;
            }
            if (!verifycationField(fieldPrice, "Price cannot be null")) {
                
            }
            var name = fieldName.getText();
            var price = 0.0;
            try {
                price = Double.parseDouble(fieldPrice.getText());
            } catch (ArithmeticException error) {
                JOptionPane.showMessageDialog(this, "The number should be Integer");
            }
            

            ProductRepository repository = new ProductRepository();
            CreateProductCommandHandler createHandler = new CreateProductCommandHandler(repository);
            
            Product product = createHandler.handle(new CreateProductCommand(name, price));
            if (product==null) {
                throw new Exception("The product is required");
            }
            JOptionPane.showMessageDialog(this, "Product "+ product.getName()+ " sucessfull save!, total "+ repository.getSizeProductDb());
            clearFields();
        } catch (Exception error){
            JOptionPane.showMessageDialog(this, error.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

